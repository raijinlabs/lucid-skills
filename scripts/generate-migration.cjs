function escSql(s) {
  return s.replace(/'/g, "''")
}

let sql = ''
sql += '-- Migration 078: Seed real plugin catalog from extracted tool manifests\n'
sql += '-- Replaces 13 fake placeholder entries with 19 real lucid-skills + lucid-defi\n'
sql += '-- Generated by: npx tsx scripts/extract-tool-manifests.ts + node scripts/generate-migration.cjs\n\n'

// DELETE fakes
sql += '-- =============================================================================\n'
sql += '-- Step 1: Remove fake placeholder entries\n'
sql += '-- =============================================================================\n\n'
sql += '-- First remove any installations/activations referencing fake plugins\n'
sql += 'DELETE FROM assistant_plugin_activations WHERE installation_id IN (\n'
sql += '  SELECT opi.id FROM org_plugin_installations opi\n'
sql += '  JOIN plugin_catalog pc ON pc.id = opi.plugin_id\n'
sql += '  WHERE pc.slug IN (' + FAKES.map(s => "'" + s + "'").join(', ') + ')\n'
sql += ');\n\n'
sql += 'DELETE FROM org_plugin_installations WHERE plugin_id IN (\n'
sql += '  SELECT id FROM plugin_catalog WHERE slug IN (' + FAKES.map(s => "'" + s + "'").join(', ') + ')\n'
sql += ');\n\n'
sql += 'DELETE FROM plugin_catalog WHERE slug IN (\n'
sql += '  ' + FAKES.map(s => "'" + s + "'").join(', ') + '\n'
sql += ');\n\n'

// UPSERT real skills
sql += '-- =============================================================================\n'
sql += '-- Step 2: Upsert all 19 real skills with extracted tool manifests\n'
sql += '-- =============================================================================\n\n'

for (const skill of d) {
  const meta = META[skill.slug]
  if (!meta) continue

  // Convert inputSchema to OpenAI function calling format (parameters)
  const tools = skill.tools.map(t => ({
    name: t.name,
    description: t.description,
    parameters: t.inputSchema,
  }))

  const manifestJson = escSql(JSON.stringify(tools))
  const descEsc = escSql(meta.desc)
  const nameEsc = escSql(meta.name)

  sql += `INSERT INTO plugin_catalog (slug, name, description, version, category, tool_manifest, source_repo, source, verified)\n`
  sql += `VALUES (\n`
  sql += `  '${skill.slug}', '${nameEsc}', '${descEsc}',\n`
  sql += `  '${meta.version}', '${meta.category}',\n`
  sql += `  '${manifestJson}'::jsonb,\n`
  sql += `  'raijinlabs/lucid-skills', 'first-party', true\n`
  sql += `)\n`
  sql += `ON CONFLICT (slug) DO UPDATE SET\n`
  sql += `  name = EXCLUDED.name,\n`
  sql += `  description = EXCLUDED.description,\n`
  sql += `  version = EXCLUDED.version,\n`
  sql += `  category = EXCLUDED.category,\n`
  sql += `  tool_manifest = EXCLUDED.tool_manifest,\n`
  sql += `  source_repo = EXCLUDED.source_repo,\n`
  sql += `  source = EXCLUDED.source,\n`
  sql += `  verified = EXCLUDED.verified;\n\n`
}

// Add lucid-defi (pure markdown, no MCP tools)
